export function getMonthHolidaysAndWorkdays(year, month) {
  const result = { holidays: [], workdays: [] };
  const monthStr = String(month).padStart(2, '0');

  for (const [dateStr, name] of Object.entries(HOLIDAYS)) {
    const [y, m] = dateStr.split('-');
    if (y === String(year) && m === monthStr) {
      result.holidays.push({ date: dateStr, name });
    }
  }

  for (const [dateStr, name] of Object.entries(WORKDAYS)) {
    const [y, m] = dateStr.split('-');
    if (y === String(year) && m === monthStr) {
      result.workdays.push({ date: dateStr, name });
    }
  }

  result.holidays.sort((a, b) => a.date.localeCompare(b.date));
  result.workdays.sort((a, b) => a.date.localeCompare(b.date));
  return result;
}