// 判断某日期是否是法定节假日（不含普通周末）
export function isHoliday(dateStr) {
  return !!HOLIDAYS[dateStr];
}

// 判断某日期是否是调休工作日（周末调整为工作日）
export function isWorkday(dateStr) {
  return !!WORKDAYS[dateStr];
}

// 获取节假日信息
export function getHolidayInfo(dateStr) {
  const name = HOLIDAYS[dateStr];
  return name ? { name } : null;
}

// 获取调休工作日信息
export function getWorkdayInfo(dateStr) {
  const name = WORKDAYS[dateStr];
  return name ? { name } : null;
}

// 获取节日名称（同时检查节假日和调休工作日）
export function getFestival(dateStr) {
  return HOLIDAYS[dateStr] || WORKDAYS[dateStr] || null;
}

