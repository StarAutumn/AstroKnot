#!/bin/bash
# ============================================================
#  AstroKnot 启动脚本 (macOS/Linux)
#  无命令框启动，自动切换到项目目录执行
# ============================================================

# 获取脚本所在目录
SCRIPT_DIR="$( cd "$( dirname "${BASH_SOURCE[0]}" )" && pwd )"

# 切换到项目目录
cd "$SCRIPT_DIR"

# 启动应用
npm start